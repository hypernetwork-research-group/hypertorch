class Node2VecGCN(nn.Module):
    """
    A joint encoder that first learns Node2Vec embeddings and then refines them with GCN layers.

    Attributes:
        node2vec: Internal Node2Vec encoder.
        gcn: GCN stack applied to Node2Vec embeddings.
    """

    def __init__(
        self,
        node2vec_config: Node2VecConfig,
        gcn_config: GCNConfig,
    ):
        """
        Initialize the joint Node2Vec-GCN encoder.

        Args:
            node2vec_config: Configuration for the internal ``Node2Vec`` encoder.
            gcn_config: Configuration for the GCN stack.
        """
        super().__init__()
        self.node2vec = Node2Vec(**node2vec_config)
        self.gcn = GCN(**gcn_config)

    def forward(
        self,
        batch: Tensor | None = None,
        edge_index: Tensor | None = None,
    ) -> Tensor:
        """
        Compute Node2Vec embeddings and refine them with GCN layers.

        Args:
            batch: Optional node IDs passed to the Node2Vec encoder.
            edge_index: Edge index used by the GCN stack. Defaults to ``None``.

        Returns:
            embeddings: GCN-refined node embeddings.

        Raises:
            ValueError: If ``edge_index`` is not provided.
        """
        if edge_index is None:
            raise ValueError("Node2VecGCN requires edge_index in forward().")

        node_embeddings = self.node2vec(batch)
        return self.gcn(node_embeddings, edge_index)

    @property
    def num_embeddings(self) -> int:
        """
        Return the number of Node2Vec embedding rows.

        Returns:
            num_embeddings: Number of node embeddings.
        """
        return self.node2vec.num_embeddings

    def loss(self, pos_rw: Tensor, neg_rw: Tensor) -> Tensor:
        """
        Compute the Node2Vec random-walk loss.

        Args:
            pos_rw: Positive random walks.
            neg_rw: Negative random walks.

        Returns:
            loss: Scalar Node2Vec loss.
        """
        return self.node2vec.loss(pos_rw, neg_rw)

    def loader(self, batch_size: int = 128, shuffle: bool = True) -> DataLoader:
        """
        Create the Node2Vec random-walk loader.

        Args:
            batch_size: Number of walk starts per batch.
            shuffle: Whether to shuffle walk starts. Defaults to ``True``.

        Returns:
            loader: Random-walk data loader.
        """
        return self.node2vec.loader(batch_size=batch_size, shuffle=shuffle)
